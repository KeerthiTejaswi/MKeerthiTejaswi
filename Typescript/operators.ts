function addNumbers(a:number,b:number) {
  return a+b;
}
var sum:number=addNumbers(3,4);
console.log('sum of two numbers is:'+sum);


function multiplyNumbers(x:number,y:number) {
  return x*y;
}
var mul:number=multiplyNumbers(3,5);
console.log('multiply of two numbers is:'+mul);

function divNumbers(i:number,j:number) {
  return i/j;
}
var division:number=divNumbers(4,2) ;
console.log('division of two numbers is :'+division);
